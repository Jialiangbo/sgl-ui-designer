const CHECKBOX_ICON_WIDTH = 26;
const CHECKBOX_ICON_HEIGHT = 22;
const checkboxIconCache = new Map();

export function getCheckboxIconDataUrl(checked, color) {
  const key = `${checked}|${color}`;
  if (checkboxIconCache.has(key)) return checkboxIconCache.get(key);

  const canvas = document.createElement('canvas');
  canvas.width = CHECKBOX_ICON_WIDTH;
  canvas.height = CHECKBOX_ICON_HEIGHT;
  const ctx = canvas.getContext('2d');
  ctx.clearRect(0, 0, CHECKBOX_ICON_WIDTH, CHECKBOX_ICON_HEIGHT);

  const data = checked ? CHECKBOX_CHECKED_BITMAP : CHECKBOX_UNCHECKED_BITMAP;
  let idx = 0;
  for (let y = 0; y < CHECKBOX_ICON_HEIGHT; y++) {
    for (let x = 0; x < CHECKBOX_ICON_WIDTH / 2; x++) {
      const byte = data[idx++];
      const a1 = (byte >> 4) & 0x0F;
      const a2 = byte & 0x0F;
      if (a1) {
        ctx.globalAlpha = a1 / 15;
        ctx.fillStyle = color;
        ctx.fillRect(x * 2, y, 1, 1);
      }
      if (a2) {
        ctx.globalAlpha = a2 / 15;
        ctx.fillStyle = color;
        ctx.fillRect(x * 2 + 1, y, 1, 1);
      }
    }
  }

  const url = canvas.toDataURL('image/png');
  checkboxIconCache.set(key, url);
  return url;
}
